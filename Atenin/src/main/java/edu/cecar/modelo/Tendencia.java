package edu.cecar.modelo;

/**
 * 
 * 
 */
public class Tendencia {
    
    private String palabraTendencia;
    private int valorTendencia;
    private int pesoTitulo;
    private int pesoContenido;

    public Tendencia() {
    }

    public String getPalabraTendencia() {
        return palabraTendencia;
    }

    public void setPalabraTendencia(String palabraTendencia) {
        this.palabraTendencia = palabraTendencia;
    }

    public int getValorTendencia() {
        return valorTendencia;
    }

    public void setValorTendencia(int valorTendencia) {
        this.valorTendencia = valorTendencia;
    }

    public int getPesoTitulo() {
        return pesoTitulo;
    }

    public void setPesoTitulo(int pesoTitulo) {
        this.pesoTitulo = pesoTitulo;
    }

    public int getPesoContenido() {
        return pesoContenido;
    }

    public void setPesoContenido(int pesoContenido) {
        this.pesoContenido = pesoContenido;
    }
    
    
       

}
